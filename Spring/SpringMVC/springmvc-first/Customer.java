package com.first;

public class Customer {
	//generate data members with same name given in the form
	String username;
	int userage;
	long userphono;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getUserage() {
		return userage;
	}
	public void setUserage(int userage) {
		this.userage = userage;
	}
	public long getUserphono() {
		return userphono;
	}
	public void setUserphono(long userphono) {
		this.userphono = userphono;
	}
}
